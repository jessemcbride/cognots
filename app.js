function formatNote(entry) {
  const note = entry.note || "";
  const match = note.match(/^Use ([^.]+) for ([^.]+)\. (.*)$/);
  if (!match) return note;
  const [, correct, english, sentenceEs] = match;
  const sentenceEn = entry.translation ||  sentenceEs;
  return `
    Use <em>${correct}</em> for <em>${english}</em>.
    <div class="note-lines">
      <span class="note-es" lang="es">${sentenceEs}</span>
      <span class="note-en" lang="en">${sentenceEn}</span>
    </div>
  `;
}

