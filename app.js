document.addEventListener("keydown", (event) => {
  if (event.code === "ArrowRight" || event.code === "Space") {
    event.preventDefault();
    next(1);
  } else if (event.code === "ArrowLeft") {
    event.preventDefault();
    next(-1);
  } else if (event.key && event.key.toLowerCase() === "s") {
    surprise();
  }
});

